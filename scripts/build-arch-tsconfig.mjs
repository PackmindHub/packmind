// scripts/build-arch-tsconfig.mjs
//
// Generates `tsconfig.arch.json` — the self-contained TypeScript config used by
// the ArchUnitTS architecture tests (packages/architecture-tests).
//
// Why a dedicated config:
//   ArchUnitTS reads the tsconfig with `ts.readConfigFile`, which does NOT resolve
//   `extends`. So the config we hand it must inline `compilerOptions.paths` +
//   `baseUrl` (to resolve `@packmind/*` aliases) AND declare an `include` (so the
//   monorepo source files are actually discovered). We derive both from the
//   edition-aware `tsconfig.base.effective.json`, generated by select-tsconfig.mjs.
//
// Output is git-ignored and rebuilt by the `arch` Nx target before each run.

import fs from 'node:fs';
import path from 'node:path';

const ROOT = process.cwd();
const EFFECTIVE = path.join(ROOT, 'tsconfig.base.effective.json');
const OUT = path.join(ROOT, 'tsconfig.arch.json');

// select-tsconfig does its work at module-evaluation time, (re)generating the
// effective config for the current PACKMIND_EDITION. This script is always run
// as its own short-lived process (the `arch` Nx target), so the dynamic import
// re-runs every invocation. Note: ESM modules are cached per-process, so a
// second import within the SAME process would not re-run it.
await import('./select-tsconfig.mjs');

if (!fs.existsSync(EFFECTIVE)) {
  console.error(
    `[build-arch-tsconfig] Expected ${path.basename(EFFECTIVE)} to exist after select-tsconfig`,
  );
  process.exit(1);
}

const effective = JSON.parse(fs.readFileSync(EFFECTIVE, 'utf8'));

// Without compilerOptions.paths the `@packmind/*` aliases cannot be resolved, so
// ArchUnitTS would build no cross-package edges and the cross-domain rules would
// pass for the wrong reason (false green). Fail loudly rather than silently.
const paths = effective.compilerOptions?.paths;
if (!paths || Object.keys(paths).length === 0) {
  console.error(
    `[build-arch-tsconfig] ${path.basename(EFFECTIVE)} has no compilerOptions.paths; ` +
      'cannot resolve @packmind/* aliases. Aborting to avoid false-green results.',
  );
  process.exit(1);
}

const arch = {
  // NB: self-contained — no `extends`, since ArchUnitTS does not resolve it.
  compilerOptions: {
    ...effective.compilerOptions,
    noEmit: true,
    skipLibCheck: true,
  },
  // Domain packages + the NestJS API layer. Specs (*.spec.ts/*.test.ts) and the
  // architecture-tests package itself are excluded so test/infra files don't
  // pollute the scanned dependency graph.
  include: ['packages/*/src/**/*.ts', 'apps/api/src/**/*.ts'],
  exclude: [
    'node_modules',
    'dist',
    'tmp',
    'packages/architecture-tests/**',
    '**/*.spec.ts',
    '**/*.test.ts',
  ],
};

fs.writeFileSync(OUT, JSON.stringify(arch, null, 2) + '\n');
console.log(
  `[build-arch-tsconfig] wrote ${path.basename(OUT)} ` +
    `(${Object.keys(arch.compilerOptions.paths || {}).length} path aliases)`,
);
